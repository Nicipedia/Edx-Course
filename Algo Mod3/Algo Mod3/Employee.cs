namespace Algo_Mod3
{
  
    public class Employee
    {
        private int empid;
        private string name;
        private string department;
        public int empID
        {
            get
            {
                return empid;
            }
            set
            {
                empid = value;
            }
        }
       
        public string Name
        {
            get
            {
                return name;
            }
            set
            {
                name = value;
            }
        }
        public string Department
        {
            get
            {
                return department;
            }
            set
            {
                department = value;
            }
        }
    }

        
}